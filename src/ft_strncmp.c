int	ft_strncmp(char *s1, char *s2, int n)
{
	int	value;
	int i;

	value = 0;
	i = 0;
	while (value == 0 && i < n && (s1[i] || s2[i]))
	{
		value += (s1[i] - s2[i]);
		i++;
	}
	return (value);
}
